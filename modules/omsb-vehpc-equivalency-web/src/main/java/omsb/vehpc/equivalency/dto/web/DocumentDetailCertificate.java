package omsb.vehpc.equivalency.dto.web;

public class DocumentDetailCertificate {
	    private String educationCertificateUrl;
	    private String educationCertificateName;
	    private String qualification;
	    private String issuedFrom;
	    private String issueDate;
	   
		public String getEducationCertificateUrl() {
			return educationCertificateUrl;
		}
		public void setEducationCertificateUrl(String educationCertificateUrl) {
			this.educationCertificateUrl = educationCertificateUrl;
		}
		public String getEducationCertificateName() {
			return educationCertificateName;
		}
		public void setEducationCertificateName(String educationCertificateName) {
			this.educationCertificateName = educationCertificateName;
		}
		public String getQualification() {
			return qualification;
		}
		public void setQualification(String qualification) {
			this.qualification = qualification;
		}
		public String getIssuedFrom() {
			return issuedFrom;
		}
		public void setIssuedFrom(String issuedFrom) {
			this.issuedFrom = issuedFrom;
		}
		public String getIssueDate() {
			return issueDate;
		}
		public void setIssueDate(String issueDate) {
			this.issueDate = issueDate;
		}
		
}
